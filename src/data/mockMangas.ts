// Função para simular carregamento de dados
export const carregarMangas = async (pagina: number = 1, limite: number = 12): Promise<{
  mangas: Manga[];
  total: number;
  pagina: number;
  limite: number;
  temMais: boolean;
}> => {
  // Simular delay de rede
  await new Promise(resolve => setTimeout(resolve, 500));
  
  const inicio = (pagina - 1) * limite;
  const fim = inicio + limite;
  const mangasPagina = mockMangas.slice(inicio, fim);
  
  return {
    mangas: mangasPagina,
    total: mockMangas.length,
    pagina,
    limite,
    temMais: fim < mockMangas.length
  };
};